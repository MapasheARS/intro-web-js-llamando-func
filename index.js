// declara una variable contador con valor 5
let contador = 5
// incrementarContador() incrementa el contador por 1
function incrementaContador() {
    contador++
}
// decrementarContador() decrementa el contador por 1
function decrementarContador(){
    contador--
}
// imprimirContador() imprime el contador en la consola
function imprimirContador(){
    console.log(contador)
}
// Llama tres veces a incrementarContador()
incrementaContador()
incrementaContador()
incrementaContador()
// Llama una vez a decrementarContador()
decrementarContador()
